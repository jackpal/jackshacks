function move(dir) {
    nx = clamp(px + kDirToDeltaX[dir], 0, levelWidth-1);
    ny = clamp(py + kDirToDeltaY[dir], 0, levelHeight-1);
    pos = nx + ny * levelWidth;
    v = map[pos];
    canMove = true;
    switch (v) {
    case kSpace:
        break;
    case kDoor:
        if ( pKeys > 0) {
            pKeys -= 1;
            floodFill(pos, kDoor, kSpace);
        } else {
            canMove = false;
        }
        break;
    case kMoney:
        pScore += 100;
        break;
    case kKey:
        pKeys += 1;
        break;
    case kBomb:
        pBombs += 1;
        break;
    case kFood:
        pHealth += 100;
        break;
    case kDown:
        nextLevel();
        return;
    default:
        canMove = false;
    break;
    }
    if (canMove) {
        map[px + py * levelWidth] = kSpace;
        px = nx;
        py = ny;
        map[px + py * levelWidth] = kPlayer1;
        dirty = true;
    }
    return canMove;
}

